package com.example.sbs.aischoolparentapp.model;

public class StemModel {
    String badge_id;
    String badge_title;
    String badge_type;
    String date;
    String badge_icon;
    String status;


    public String getBadge_id() {
        return badge_id;
    }

    public void setBadge_id(String badge_id) {
        this.badge_id = badge_id;
    }

    public String getBadge_title() {
        return badge_title;
    }

    public void setBadge_title(String badge_title) {
        this.badge_title = badge_title;
    }

    public String getBadge_type() {
        return badge_type;
    }

    public void setBadge_type(String badge_type) {
        this.badge_type = badge_type;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getBadge_icon() {
        return badge_icon;
    }

    public void setBadge_icon(String badge_icon) {
        this.badge_icon = badge_icon;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
